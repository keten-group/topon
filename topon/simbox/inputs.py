# ======================================================================
# Stage 4 – Crosslink template
# ======================================================================

def _write_crosslink_template(
    path: Path, system: AssembledSystem, temperature: float
) -> None:
    # Collect unique reactive group names for documentation
    reactive_groups = sorted(set(e.group_name for e in system.reactive_sites))
    groups_doc = ", ".join(reactive_groups) if reactive_groups else "(none detected)"

    script = f"""\
# =============================================================
# Stage 4b: Crosslink reaction
# Generated by topon.simbox
#
# Detected reactive groups: {groups_doc}
#
# This file provides commands for two crosslinking
# approaches.  Uncomment and configure the one you need.
# =============================================================

units           real
atom_style      full
boundary        p p p

read_restart    restart.npt

pair_style      lj/cut 12.0
pair_modify     mix arithmetic

neighbor        2.0 bin
neigh_modify    every 1 delay 0 check yes

# Include reactive-group definitions
include         groups.txt

thermo          500
thermo_style    custom step temp pe ke etotal press vol density

fix             rxn_nvt all nvt temp {temperature} {temperature} 100.0
timestep        1.0

# ==============================================================
# OPTION A: fix bond/react  (template-based, most flexible)
# ==============================================================
# You need pre-reaction and post-reaction molecule templates.
# See: https://docs.lammps.org/fix_bond_react.html
#
# fix  rxn all bond/react stabilization yes nvt_grp &
#      react epoxy_amine all 100 3.5 &
#          pre_react_template.data post_react_template.data &
#          map_file.txt
#
# -- Replace the file names with your actual templates --

# ==============================================================
# OPTION B: fix bond/create  (simpler, distance-based)
# ==============================================================
# Creates bonds between atom types within a cutoff distance.
# See: https://docs.lammps.org/fix_bond_create.html
#
# fix  bc all bond/create 100 <type_epoxide_C> <type_amine_N> &
#      3.5 <new_bond_type> prob 0.5 {int(temperature)}
#
# -- Replace <type_...> with the LAMMPS numeric atom types
#    from your data file --

# ==============================================================
# Production run (with reaction fix active)
# ==============================================================
# run  500000

# unfix rxn_nvt
# write_data system_crosslinked.data
# write_restart restart.crosslinked

print "Stage 4 template loaded – configure crosslink fix above"
"""
    with open(path, "w") as f:
        f.write(script)
